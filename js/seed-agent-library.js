        '    var args = $.global.__holyScriptureAgentArgs || {};',
        '    var result = { success: false, message: "", comps: [] };',
        '    function trimComp(comp, padding, recursive) {',
        '        var i;',
        '        if (!comp || !(comp instanceof CompItem) || comp.numLayers === 0) return;',
        '        var minIn = 999999999;',
        '        var maxOut = -999999999;',
        '        for (i = 1; i <= comp.numLayers; i++) {',
        '            var layer = comp.layer(i);',
        '            if (layer.inPoint < minIn) minIn = layer.inPoint;',
        '            if (layer.outPoint > maxOut) maxOut = layer.outPoint;',
        '        }',
        '        if (minIn === 999999999 || maxOut === -999999999) return;',
        '        var newStart = Math.max(0, minIn - padding);',
        '        var newDuration = (maxOut + padding) - newStart;',
        '        if (newDuration <= 0) return;',
        '        comp.workAreaStart = newStart;',
        '        comp.workAreaDuration = newDuration;',
        '        comp.duration = newStart + newDuration;',
        '        result.comps.push({ name: comp.name, duration: Math.round(newDuration * 1000) / 1000, start: Math.round(newStart * 1000) / 1000 });',
        '        if (recursive) {',
        '            for (i = 1; i <= comp.numLayers; i++) {',
        '                try { if (comp.layer(i).source instanceof CompItem) trimComp(comp.layer(i).source, padding, true); } catch (nestedErr) {}',
        '            }',
        '        }',
        '    }',
        '    try {',
        '        var comp = app.project.activeItem;',
        '        if (!comp || !(comp instanceof CompItem)) return JSON.stringify({ success: false, error: "No active composition" });',
        '        app.beginUndoGroup("Holy Scripture Agent: Trim Comp");',
        '        trimComp(comp, args.padding || 0, args.recursive === true);',
        '        app.endUndoGroup();',
        '        result.success = result.comps.length > 0;',
        '        result.message = "Trimmed " + result.comps.length + " comp(s)";',
