        '            var layer = layers[i];',
        '            try {',
        '                var rect = layer.sourceRectAtTime(comp.time, false);',
        '                if (rect.width <= 0 || rect.height <= 0) { skip(layer.name, "Layer has no measurable bounds"); continue; }',
        '                var scaleProp = layer.property("ADBE Transform Group").property("ADBE Scale");',
        '                var posProp = layer.property("ADBE Transform Group").property("ADBE Position");',
        '                var anchor = layer.property("ADBE Transform Group").property("ADBE Anchor Point").value;',
        '                var currentScale = scaleProp.value;',
        '                var actualW = rect.width * (currentScale[0] / 100);',
        '                var actualH = rect.height * (currentScale[1] / 100);',
        '                var scaleX = currentScale[0];',
        '                var scaleY = currentScale[1];',
        '                if (mode === "fill") {',
        '                    var fillRatio = Math.max(comp.width / actualW, comp.height / actualH);',
        '                    scaleX = currentScale[0] * fillRatio;',
        '                    scaleY = currentScale[1] * fillRatio;',
        '                } else if (mode === "stretch") {',
        '                    scaleX = currentScale[0] * (comp.width / actualW);',
        '                    scaleY = currentScale[1] * (comp.height / actualH);',
        '                } else {',
        '                    var fitRatio = Math.min(comp.width / actualW, comp.height / actualH);',
        '                    scaleX = currentScale[0] * fitRatio;',
        '                    scaleY = currentScale[1] * fitRatio;',
        '                }',
        '                scaleProp.setValue([scaleX, scaleY]);',
        '                var rectCenter = [rect.left + (rect.width / 2), rect.top + (rect.height / 2)];',
        '                posProp.setValue([comp.width / 2 + ((rectCenter[0] - anchor[0]) * (scaleX / 100)), comp.height / 2 + ((rectCenter[1] - anchor[1]) * (scaleY / 100))]);',
        '                result.layers.push({ name: layer.name, scaleX: Math.round(scaleX * 10) / 10, scaleY: Math.round(scaleY * 10) / 10 });',
        '            } catch (layerErr) {',
        '                skip(layer.name, layerErr.toString());',
        '            }',
        '        }',
        '        app.endUndoGroup();',
        '        result.success = result.layers.length > 0;',
        '        result.message = "Adjusted " + result.layers.length + " layer(s)";',
