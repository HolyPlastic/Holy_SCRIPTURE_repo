        '    var result = { success: false, message: "", layers: [] };',
        '    function bubbleSortLayers(arr) {',
        '        var a, b, temp;',
        '        for (a = 0; a < arr.length - 1; a++) {',
        '            for (b = a + 1; b < arr.length; b++) {',
        '                if (arr[b].index < arr[a].index) { temp = arr[a]; arr[a] = arr[b]; arr[b] = temp; }',
