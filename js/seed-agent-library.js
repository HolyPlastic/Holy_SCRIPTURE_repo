        '        var i;',
        '        for (i = 1; i <= group.numProperties; i++) {',
        '            var prop = group.property(i);',
        '            if (prop.propertyType === PropertyType.PROPERTY) {',
        '                if (prop.numKeys > 0) {',
        '                    var keys = [];',
        '                    var k;',
        '                    for (k = 1; k <= prop.numKeys; k++) keys.push({ time: prop.keyTime(k), value: prop.keyValue(k) });',
        '                    for (k = prop.numKeys; k >= 1; k--) prop.removeKey(k);',
        '                    for (k = 0; k < keys.length; k++) prop.setValueAtTime(keys[k].time + amount, keys[k].value);',
        '                }',
        '            } else if (prop.propertyType === PropertyType.INDEXED_GROUP || prop.propertyType === PropertyType.NAMED_GROUP) {',
        '                shiftPropertyKeys(prop, amount);',
        '            }',
        '        }',
        '    }',
